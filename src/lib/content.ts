export async function getArticles(): Promise<Article[]> {
  const db = await dbListArticles();
  if (db) {
    return dedupeArticlesForDisplay(
      db
        .map(articleFromRow)
        .filter((a) => !shouldHideEmptyFacebookShell(a)),
    );
  }
  return (articlesData as Article[])
    .slice()
    .sort(
      (a, b) =>
        new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime()
    );
}

export async function getFeaturedArticles(): Promise<Article[]> {
  const all = await getArticles();
  return all.filter((a) => a.featured);
}

export async function getArticleBySlug(
  slug: string
): Promise<Article | undefined> {
  const db = await dbGetArticleBySlug(slug);
  if (db) {
    const article = articleFromRow(db);
    if (
      slug.startsWith("mooreanews-fb-") &&
      !hasPersistedCoverUrl(article.image)
    ) {
      article.image = `/api/facebook-cover?slug=${encodeURIComponent(slug)}`;
    }
    return article;
  }
  return (articlesData as Article[]).find((a) => a.slug === slug);
}

