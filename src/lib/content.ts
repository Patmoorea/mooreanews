export async function getAnnouncements(): Promise<Announcement[]> {
  const db = await dbListAnnouncements();
  const raw = db
    ? db
        .filter((r) => r.category !== "covoiturage")
        .filter(isAnnouncementVisible)
        .map(announcementFromRow)
    : (announcementsData as Announcement[]).filter((a) =>
        isAnnouncementVisible({ created_at: a.publishedAt, expires_at: null }),
      );
  return raw.slice().sort((a, b) => {
    const aBoost = a.boosted ? 1 : 0;
    const bBoost = b.boosted ? 1 : 0;
    if (aBoost !== bBoost) return bBoost - aBoost;
    return (
      new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime()
    );
  });
}

export async function getAnnouncementBySlug(
  slug: string,
): Promise<Announcement | undefined> {
  const all = await getAnnouncements();
  return all.find((a) => a.slug === slug);
}

export async function getCarpoolOffers(): Promise<ParsedCarpoolOffer[]> {
  const db = await dbListAnnouncements();
  if (!db) return [];

  const rows = db.filter(
    (r) => r.category === "covoiturage" && isAnnouncementVisible(r),
  );
  const signupCounts = await getCarpoolSignupCounts(rows.map((r) => r.id));

  return rows
    .map((row) => {
      const parsed = parseCarpoolBody(row.body);
      const seats = parsed.seats ?? 1;
      const signupCount = signupCounts[row.id] ?? 0;
      return {
        id: row.id,
        title: row.title,
        publishedAt: row.created_at,
        contact: row.contact ?? "",
        phone: row.contact ?? "",
        author: row.author ?? "",
        direction: parsed.direction ?? "vers-quai",
        tripDate: parsed.tripDate ?? "",
        time: parsed.time ?? "",
        tripDays: parsed.tripDays,
        seats,
        signupCount,
        seatsLeft: Math.max(0, seats - signupCount),
        meetingPoint: parsed.meetingPoint ?? row.district ?? "",
        destination: parsed.destination ?? "",
        priceShare: parsed.priceShare,
        notes: undefined,
      };
    })
    .sort((a, b) => {
      const byDate = a.tripDate.localeCompare(b.tripDate);
      if (byDate !== 0) return byDate;
      return a.time.localeCompare(b.time);
    });
}

