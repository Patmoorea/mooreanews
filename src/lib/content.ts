function hasPersistedCoverUrl(url: string | null | undefined): boolean {
  const u = url?.trim() ?? "";
  if (!u.startsWith("http")) return false;
  if (isFacebookCdnCoverUrl(u)) return false;
  return true;
}

/** Masque les coquilles Facebook vides (texte + affiche absents). */
function shouldHideEmptyFacebookShell(a: Article): boolean {
  if (!a.slug.startsWith("mooreanews-fb-")) return false;
  if (hasPersistedCoverUrl(a.image)) return false;
  return isFacebookAutoGeneratedPlaceholder({
    title: a.title,
    excerpt: a.excerpt ?? null,
    body: a.body,
  });
}

function articleFromRow(r: ArticleRow): Article {
  let image = r.cover_url ?? undefined;
  const isBrokenCover =
    !image?.trim() ||
    image.includes("fbcdn.net") ||
    image.includes("fbsbx.com");
  if (r.slug?.startsWith("garde-moorea-") && isBrokenCover) {
    const validFrom = r.slug.slice("garde-moorea-".length);
    image =
      resolveGardePosterPublicUrl(r.cover_url) ??
      `${SITE.url.replace(/\/$/, "")}/api/garde-weekend/poster/${validFrom}`;
  }
  // Pas de fallback /api/facebook-cover en liste — évite l’icône « image cassée ».
  return {
    slug: r.slug,
    title: r.title,
    excerpt: r.excerpt,
    body: r.body,
    category: normalizeArticleCategory(r.category),
    tags: r.tags ?? undefined,
    image,
    author: r.author ?? undefined,
    featured: r.featured,
    publishedAt: r.published_at,
  };
}

const ICPF_AFFICHE = "/images/events/depistage-cancer-peau-icpf-2026.jpg";

/** Affiches locales quand cover_url n’est pas encore en base (clé = titre normalisé). */
const EVENT_TITLE_COVERS: Record<string, string> = {
  "depistage de la peau": ICPF_AFFICHE,
};

function eventCoverFromRow(r: EventRow): string | undefined {
  const url = r.cover_url?.trim();
  if (url) return url;
  const key = normalizeTitleKey(r.title);
  if (EVENT_TITLE_COVERS[key]) return EVENT_TITLE_COVERS[key];
  if (key.includes("depistage") && key.includes("peau")) return ICPF_AFFICHE;
  return undefined;
}

function eventFromRow(r: EventRow): Event {
  return {
    slug: r.id,
    title: r.title,
    description: r.description,
    category: normalizeEventCategory(r.category),
    date: r.date,
    endDate: r.end_date ?? undefined,
    time: r.start_time ?? undefined,
    location: r.location,
    district: r.district ?? undefined,
    organizer: r.organizer ?? undefined,
    price: r.price ?? undefined,
    contact: r.contact ?? undefined,
    url: r.url ?? undefined,
    image: eventCoverFromRow(r),
  };
}

function announcementFromRow(r: AnnouncementRow): Announcement {
  const boosted =
    r.boosted_until != null && new Date(r.boosted_until).getTime() > Date.now();
  return {
    slug: r.id,
    title: r.title,
    body: r.body,
    type: normalizeAnnouncementType(r.category),
    district: r.district ?? undefined,
    price: r.price ?? undefined,
    contact: r.contact ?? "",
    publishedAt: r.created_at,
    image: r.cover_url ?? undefined,
    boosted,
  };
}

function restaurantFromRow(r: RestaurantRow): Restaurant {
  const premium = isPremiumRestaurant(r);
  return {
    slug: r.id,
    name: r.name,
    description: r.description,
    cuisine: r.cuisine,
    priceLevel: priceLevelFromRange(r.price_range),
    district: r.district,
    address: r.address,
    phone: r.phone ?? undefined,
    website: r.url ?? undefined,
    openingHours:
      catalogOpeningHoursForRestaurant(r.name, r.id) ||
      r.hours?.trim() ||
      undefined,
    image: r.cover_url ?? undefined,
    premium,
    lat: r.lat ?? undefined,
    lon: r.lon ?? undefined,
    googlePlaceId: r.google_place_id ?? undefined,
    merchantOpenStatus: r.merchant_open_status ?? undefined,
    merchantOpenUpdatedAt: r.merchant_open_updated_at ?? undefined,
    merchantEmail: r.merchant_email ?? undefined,
  };
}

function isPremiumRestaurant(r: RestaurantRow): boolean {
  if (r.featured) return true;
  if (!r.premium_until) return false;
  return new Date(r.premium_until).getTime() > Date.now();
}

export function restaurantToOpenMeta(r: Restaurant): RestaurantOpenMeta {
  return {
    slug: r.slug,
    name: r.name,
    district: r.district,
    googlePlaceId: r.googlePlaceId,
    merchantOpenStatus: r.merchantOpenStatus,
    merchantOpenUpdatedAt: r.merchantOpenUpdatedAt,
  };
}

function priceLevelFromRange(range: string | null): 1 | 2 | 3 | 4 {
  if (!range) return 2;
  const n = Number.parseInt(range, 10);
  if (n >= 1 && n <= 4) return n as 1 | 2 | 3 | 4;
  const symbols = (range.match(/€|\$/g) ?? []).length;
  return Math.min(4, Math.max(1, symbols || 2)) as 1 | 2 | 3 | 4;
}

